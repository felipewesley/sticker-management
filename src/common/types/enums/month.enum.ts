/**
 * @enum Month
 * Months enumeration - Used to keep a legible code, cause `JS Date` object use a different month convention.
 * 
 * Example:
 * - Common convention: `january => 1 | december => 12`
 * - `JS Date` convention: `january => 0 | december => 11`
 */
export enum Month {

    January = 0,
    February = 1,
    March = 2,
    April = 3,
    May = 4,
    June = 5,
    July = 6,
    August = 7,
    September = 8,
    October = 9,
    November = 10,
    December = 11

}